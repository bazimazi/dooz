
const WIN_LINES = [
    [[0, 0], [0, 1], [0, 2]],
    [[1, 0], [1, 1], [1, 2]],
    [[2, 0], [2, 1], [2, 2]],

    [[0, 0], [1, 0], [2, 0]],
    [[0, 1], [1, 1], [2, 1]],
    [[0, 2], [1, 2], [2, 2]],

    [[0, 0], [1, 1], [2, 2]],
    [[0, 2], [1, 1], [2, 0]],
];

export function checkBoard(board: string[][], currentPlayer: string, winLines: number[][][]): number[][] | void {

    return winLines.find(line => line.every(cell => board[cell[0]][cell[1]] == currentPlayer))

}